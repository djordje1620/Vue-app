<template>
  <div>
    <p>Logging out...</p>
  </div>
</template>

<script>
import { mapActions } from 'vuex';

export default {
  async created() {
    // Pozovite Vuex akciju za odjavu
    await this.logout();
    // Preusmerite korisnika nakon uspe≈°ne odjave
    this.$router.push('/home'); 
  },
  methods: {
    ...mapActions(['logout']),
  },
};
</script>
